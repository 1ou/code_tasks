    public static int runBinarySearchIteratively2(long[] sortedArray, long key, int low, int high) {
        while (low <= high) {
            int mid = (low + high) / 2;
            if (sortedArray[mid] < key) {
                low = mid + 1;
            } else if (sortedArray[mid] > key) {
                high = mid - 1;
            } else if (sortedArray[mid] == key) {
                return mid + 1;
            }
        }
        return Math.max(low, 0);
    }

    public static int runBinarySearchIteratively(long[] sortedArray, long key, int low, int high) {
        while (low <= high) {
            int mid = (low + high) / 2;
            if (sortedArray[mid] < key) {
                low = mid + 1;
            } else if (sortedArray[mid] > key) {
                high = mid - 1;
            } else if (sortedArray[mid] == key) {
                while (mid < high - 1000 && sortedArray[mid + 999] == sortedArray[mid]) mid+=999;
                while (mid < high && sortedArray[mid + 1] == sortedArray[mid]) mid++;
                return mid + 1;
            }
        }
        return Math.max(low, 0);
    }

    static void solve() {
        int n = FS.nextInt();
        long[] x = FS.readArrayL(n);
        int q = FS.nextInt();
        Arrays.sort(x);
        for (int i = 0; i < q; i++) {
            long m = FS.nextLong();
            FS.pt.println(runBinarySearchIteratively(x, m, 0, n - 1));
        }
    }

